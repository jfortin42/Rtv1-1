t_m44	ft_new_identity_m44(void)
{
	t_m44	m;

	m.m[0] = 1;
	m.m[1] = 0;
	m.m[2] = 0;
	m.m[3] = 0;
	m.m[4] = 0;
	m.m[5] = 1;
	m.m[6] = 0;
	m.m[7] = 0;
	m.m[8] = 0;
	m.m[9] = 0;
	m.m[10] = 1;
	m.m[11] = 0;
	m.m[12] = 0;
	m.m[13] = 0;
	m.m[14] = 0;
	m.m[15] = 1;
	return (m);
}

t_m44	ft_mul_m44(t_m44 m1, t_m44 m2)
{
	int		i;
	int		j;
	int		k;
	float	sum;
	t_m44	m;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			sum = 0.0f;
			k = 0;
			while (k < 4)
			{
				sum += m1.m[i * 4 + k] * m2.m[k * 4 + j];
				k++;
			}
			m.m[i * 4 + j] = sum;
			j++;
		}
		i++;
	}
	return (m);
}
